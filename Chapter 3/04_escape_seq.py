# \n used for next line 
x = "harry went upto the hill.\nHe is good \"boy\""
print(x)

#\'
print('It\'s a sunny day')

#\r
'''The \r escape sequence in Python represents a carriage return. Its effect is to move the 
cursor back to the beginning of the current line, without advancing to the next
line. Any characters after the \r will overwrite the characters that were previously printed on the same line.'''
print("Hello\rWorld")

# \xhh - hexadecimal value code
print("\x48\x65\x6c\x6c\x6f")  # Outputs 'Hello'

